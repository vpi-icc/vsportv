<p>«Спортивно-студенческий» экологический патруль высадился на улице Медведева. В течение двух часов, несмотря на ненастную погоду ребята собрали более четырех кубометров мусора – бытовые отходы, пластик, ПЭТ – бутылки, использованные покрышки, разбитую мебель и строительный мусор.</p>
