<p>Мероприятие стало составной частью Всероссийской экологической акции «Сделаем вместе!», которая стартовала 14 сентября во всех регионах нашей страны. В нашем городе было определено несколько ключевых территорий, которые решено было привести в порядок усилиями горожан. При этом акцент был сделан на активное участие в акции молодежи, студентов, спортсменов, деятелей культуры.</p>
